    /**
     * Returns a tag list for the user.
     *
     * @param UserInterface $user
     *
     * @return Tag[]
     */
    public function findByUser(UserInterface $user): array
    {
        return $this->createQueryBuilder('t')
            ->andWhere('t.user = :user')
            ->setParameter('user', $user)
            ->orderBy('t.name', 'ASC')
            ->getQuery()
            ->getResult();
    }

